package com.arnaldo.malezapp.recyclerViewGrid;

public class ItemRVG {

    private String nombre;
    private String imagenURL;
    private int imagenLocal;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String android_version_name) {
        this.nombre = android_version_name;
    }

    public String getImagenURL() {
        return imagenURL;
    }

    public void setImagenURL(String android_image_url) {
        this.imagenURL = android_image_url;
    }

    public int getImagenLocal() {
        return imagenLocal;
    }

    public void setImagenLocal(int imagenLocal) {
        this.imagenLocal = imagenLocal;
    }

}